# this is a normal text 
text = "   hello word!     "
print(text)


# using strip method

text = "   hello word!    "
strip_text = text.strip()
print(strip_text)

# wild space right remove 

y = "   hello word!    "
wildstrip_text = y.rstrip()
print(wildstrip_text)

# wild space for left remove 

x = "   hello word!    "
wildstrip_text = x.lstrip()
print(wildstrip_text)

